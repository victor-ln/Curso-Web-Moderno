/**
 * Concatenates string with number.
 */
console.log('50' + 2);

/**
 * Converts string to number.
 */
console.log('0xff' / 10);
console.log('50' - 2);

/**
 * Returns NaN (Not a Number);
 */
console.log('Testing' * 5);

/**
 * ECMA-262
 * IEEE Standard for Binary Floating-Arithmetic
 */
console.log(0.7 + 0.1);
console.log(0.2 + 0.1);
console.log(0.1 + 0.1);
console.log(0.9 + 0.1);

/**
 * console.log(10.toString()); <-- It Fails!
 */
console.log((10).toString());
